 <script>
     function formatVietnamDong(amount) {
         return amount.toLocaleString('vi-VN', {
             style: 'currency',
             currency: 'VND'
         });
     }

     function redirectCheckout() {
         const cartItems = JSON.parse(localStorage.getItem('cart')) || [];
         if (cartItems.length == 0) {
             Error('Chưa có sản phẩm trong giỏ hàng.')
             return;
         } else {
             window.location.href = "{{ route('thanh-toan.index') }}";
         }
     }

     function addCart(id) {
         $.ajax({
             url: '{{ route('home') }}/api/products/' + id,
             success: function(response) {
                 if (response.status == 200) {
                     let cartItems = [];
                     const existingCart = localStorage.getItem('cart');
                     if (existingCart) {
                         cartItems = JSON.parse(existingCart);
                         const existingProduct = cartItems.find(item => item.id === response.data.id);
                         if (existingProduct) {
                             if (existingProduct.quantity < response.data.in_stock) {
                                 existingProduct.quantity += 1;
                             } else {
                                 Error('Số lượng sản phẩm trong kho không đủ');
                                 return;
                             }
                         } else {
                             if (1 > response.data.in_stock) {
                                 Error('Số lượng sản phẩm trong kho không đủ');
                                 return;
                             }
                             cartItems.push(response.data);
                         }
                     } else {
                         if (1 > response.data.in_stock) {
                             Error('Số lượng sản phẩm trong kho không đủ');
                             return;
                         }
                         cartItems.push(response.data);
                     }
                     localStorage.setItem('cart', JSON.stringify(cartItems));
                     Success('Thêm vào giỏ hàng thành công');
                     renderCartHeader()
                 } else {
                     Error('Thêm thất bại');
                 }
             },
             error: function(error) {
                 if (error.status == 429) {
                     Error('Ban đang ấn quá nhanh, vui lòng thử lại sau 1 phút.');
                 } else {
                     Error('Thêm sản phẩm thất bại');
                 }
             }
         });
     }

     function addCartQuality(id) {
         let quantity = $('#quantyCart_' + id).val();
         if (quantity < 1) {
             Error('Số lượng phải là số và lớn hơn 0');
             return;
         } else if (isNaN(quantity)) {
             Error('Số lượng phải là số và lớn hơn 0');
             return;
         }
         $.ajax({
             url: '{{ route('home') }}/api/products/' + id,
             success: function(response) {
                 if (response.status == 200) {
                     let cartItems = [];
                     const existingCart = localStorage.getItem('cart');
                     if (existingCart) {
                         cartItems = JSON.parse(existingCart);
                         const existingProduct = cartItems.find(item => item.id === response.data.id);
                         if (existingProduct) {
                             if (existingProduct.quantity + parseInt(quantity) > response.data.in_stock) {
                                 Error('Số lượng sản phẩm trong kho không đủ');
                                 return;
                             }
                             existingProduct.quantity += parseInt(quantity);
                         } else {
                             if (parseInt(quantity) > response.data.in_stock) {
                                 Error('Số lượng sản phẩm trong kho không đủ');
                                 return;
                             }
                             response.data.quantity = parseInt(quantity);
                             cartItems.push(response.data);
                         }
                     } else {
                         response.data.quantity = parseInt(quantity);
                         cartItems.push(response.data);
                     }
                     localStorage.setItem('cart', JSON.stringify(cartItems));
                     Success('Thêm vào giỏ hàng thành công');
                     renderCartHeader()
                 } else {
                     Error('Thêm thất bại');
                 }
             },
             error: function(error) {
                 if (error.status == 429) {
                     Error('Ban đang ấn quá nhanh, vui lòng thử lại sau 1 phút.');
                 } else {
                     Error('Thêm sản phẩm thất bại');
                 }
             }
         });
     }

     function renderCartHeader() {
         if (localStorage.getItem('cart')) {
             const cartItems = JSON.parse(localStorage.getItem('cart'));

             const cartWrapper = $('.cart-product-wrapper');

             const cartHtmls = cartItems.map(item => {
                 return `
                           <div class="cart-product-inner p-b-20 m-b-20 border-bottom">
                               <div class="single-cart-product">
                                   <div class="cart-product-thumb">
                                       <a href="{{ route('home') }}/san-pham/${item.slug}"><img src="${item.image_main}" alt="${item.name}"></a>
                                   </div>
                                   <div class="cart-product-content">
                                       <h3 class="title"><a href="{{ route('home') }}/san-pham/${item.slug}">${item.name}</a></h3>
                                       <div class="product-quty-price">
                                           <span class="cart-quantity">Số lượng: <strong> ${item.quantity} </strong></span>
                                           <span class="price">
                                           <span class="new">${formatVietnamDong(item.price)}</span>
                                           </span>
                                       </div>
                                   </div>
                               </div>
                               <div class="cart-product-remove">
                                   <a href="javascript:deleteCartHeader(${item.id})"><i class="icon-close"></i></a>
                               </div>
                           </div>`;
             });

             cartWrapper.html(cartHtmls);
             $('.header-action-num').html(cartItems.length);
         } else {
             $('#cart-product').html('');
             $('.cart-product-wrapper').html('');
             $('.header-action-num').html('0');
         }
         totalCartMoney()
     }

     function deleteCartHeader(id) {
         const cartItems = JSON.parse(localStorage.getItem('cart'));
         const newCartItems = cartItems.filter(item => item.id !== id);
         localStorage.setItem('cart', JSON.stringify(newCartItems));
         renderCartHeader();
         renderCart();
     }

     function totalCartMoney() {
         const cartItems = JSON.parse(localStorage.getItem('cart'));
         let total = 0;
         if (cartItems) {
             cartItems.forEach(item => {
                 total += item.price * item.quantity;
             });
         }
         $('#total-cart-money').html(formatVietnamDong(total));
         $('#totalMoney').html(formatVietnamDong(total));
         $('#totalMoneyFinal').html(formatVietnamDong(total));
     }

     renderCartHeader()
     totalCartMoney()
